    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        int branchCode = Integer.parseInt(txtBranchCode.getText().trim());
        String branchName = txtBranchName.getText().trim();
        String telephone = txtTelephone.getText().trim();
        String fax = txtFax.getText().trim();
        String email = txtEmail.getText().trim();
        String address = txtAddress.getText().trim();

        Branch tempBranch = new Branch(branchCode, branchName, telephone, fax, email, address);

        try {
            branchDAC.updateBranch(tempBranch, currentBranchCode);
        } catch (SQLException ex) {
            Logger.getLogger(AddNewBranch.class.getName()).log(Level.SEVERE, null, ex);
        }
        dispose();
    }//GEN-LAST:event_btnUpdateActionPerformed

    public void populateGUI(Branch branch) {
        int branchCode = branch.getBranchCode();
        String branchCodeS = Integer.toString(branchCode);
        if (branchCode < 100) {
            branchCodeS = "0" + branchCodeS;
        }
        txtBranchCode.setText(branchCodeS);
        txtBranchName.setText(branch.getBranchName());
        txtTelephone.setText(branch.getTelephone());
        txtFax.setText(branch.getFax());
        txtEmail.setText(branch.getEmail());
        txtAddress.setText(branch.getAddress());

        this.currentBranchCode = branch.getBranchCode();
        btnAdd.setVisible(false);
        btnUpdate.setVisible(true);

        // Listen for changes in the text
        txtBranchCode.getDocument().addDocumentListener(myDocumentListener);
        txtBranchName.getDocument().addDocumentListener(myDocumentListener);
        txtTelephone.getDocument().addDocumentListener(myDocumentListener);
        txtEmail.getDocument().addDocumentListener(myDocumentListener);
        txtFax.getDocument().addDocumentListener(myDocumentListener);
        txtAddress.getDocument().addDocumentListener(myDocumentListener);

    }

