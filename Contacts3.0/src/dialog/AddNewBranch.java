    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        try {
            int branchCode = Integer.parseInt(txtBranchCode.getText().trim());
            String branchName = txtBranchName.getText().trim();
            String telephone = txtTelephone.getText().trim();
            String fax = txtFax.getText().trim();
            String email = txtEmail.getText().trim();
            String address = txtAddress.getText().trim();

            Branch tempBranch = new Branch(branchCode, branchName, telephone, fax, email, address);

            branchDAC.addBranch(tempBranch);

        } catch (SQLException ex) {
            Logger.getLogger(AddNewBranch.class.getName()).log(Level.SEVERE, null, ex);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "The Branch Code is not valid !!!", "Input Error", JOptionPane.WARNING_MESSAGE);
            txtBranchCode.setText("");
            return;
        }
        dispose();
    }//GEN-LAST:event_btnAddActionPerformed

