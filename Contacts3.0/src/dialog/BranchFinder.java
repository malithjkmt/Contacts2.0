    private void setFields(Branch branch) {
        int branchCode = branch.getBranchCode();
        String bc = Integer.toString(branchCode);
        if (branchCode < 100) {
            bc = "0" + bc;
        }
        autoComboBranchCode.setSelectedItem(bc);
        autoComboBranchName.setSelectedItem(branch.getBranchName());
        txtTel.setText(branch.getTelephone());
        txtFax.setText(branch.getFax());
        txtEmail.setText(branch.getEmail());
        txtAreaAddress.setText(branch.getAddress());
    }

