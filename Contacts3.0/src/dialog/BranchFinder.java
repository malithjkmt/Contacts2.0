    private void btnSearchForNameActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchForNameActionPerformed
        try {
            Branch branch = branchDAC.searchBranch(autoComboBranchCode.getSelectedItem().toString());
            setFields(branch);
        } catch (NumberFormatException e1) {
            JOptionPane.showMessageDialog(rootPane, "Please enter a valid Branch Code", "Input Error", JOptionPane.WARNING_MESSAGE);
        } catch (SQLException e2) {
            JOptionPane.showMessageDialog(rootPane, "No results for branch code : " + autoComboBranchCode.getSelectedItem().toString(), "Search Results", JOptionPane.INFORMATION_MESSAGE);

        } catch (Exception ex) {
            Logger.getLogger(BranchFinder.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSearchForNameActionPerformed

